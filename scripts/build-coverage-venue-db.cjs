function buildProviders() {
  const providers = [];
  const seenIds = new Set();
  for (const category of CATEGORIES) {
    for (const borough of BOROUGHS) {
      for (const neighborhood of HOODS[borough]) {
        const gapKey = `${category}|${borough}|${neighborhood}`;
        const cell = PROVIDER_CELLS[gapKey];
        if (!cell) throw new Error(`Missing provider cell: ${gapKey}`);
        if (seenIds.has(cell.id)) throw new Error(`Duplicate provider id: ${cell.id}`);
        seenIds.add(cell.id);
        providers.push({
          gapKey,
          ...cell,
          category,
          borough,
          neighborhood,
        });
      }
    }
  }
  return providers;
}

function buildMeetups() {
  const meetups = [];
  const seenIds = new Set();

  for (const groupType of ["Nightlife Crew", "Local Creators"]) {
    const row = MEETUP_BY_GROUP_TYPE[groupType];
    if (seenIds.has(row.id)) throw new Error(`Duplicate meetup id: ${row.id}`);
    seenIds.add(row.id);
    meetups.push({ ...row, groupType });
  }

  let hoodIndex = 0;
  for (const borough of BOROUGHS) {
    for (const neighborhood of HOODS[borough]) {
      const gapKey = `${borough}|${neighborhood}`;
      const row = MEETUP_BY_HOOD[gapKey];
      if (!row) throw new Error(`Missing meetup cell: ${gapKey}`);
      if (seenIds.has(row.id)) throw new Error(`Duplicate meetup id: ${row.id}`);
      seenIds.add(row.id);
      meetups.push({
        gapKey,
        ...row,
        borough,
        neighborhood,
      });
      hoodIndex += 1;
    }
  }

  for (const gt of MEETUP_GROUP_TYPES) {
    if (!meetups.some((m) => m.groupType === gt)) {
      throw new Error(`No meetup covers groupType: ${gt}`);
    }
  }

  return meetups;
}

function main() {
  const providers = buildProviders();
  const meetups = buildMeetups();
  const outPath = path.join(__dirname, "coverage-venue-db.json");
  fs.writeFileSync(outPath, JSON.stringify({ providers, meetups }, null, 2));
  console.log(
    JSON.stringify({
      path: outPath,
      providers: providers.length,
      meetups: meetups.length,
    }),
  );
}

main();

